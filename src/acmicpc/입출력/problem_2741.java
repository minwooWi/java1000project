package acmicpc.입출력;

import java.util.Scanner;

public class problem_2741 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int input = sc.nextInt();
        sc.close();

        for (int i = 1; i <= input; ++i) {
            System.out.println(i);
        }
    }
}